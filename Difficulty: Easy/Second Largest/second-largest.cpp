// User function template for C++
class Solution {
  public:
    // Function returns the second
    // largest elements
    int print2largest(vector<int> &arr) {
        // Code Here
        int ans = -1;
        int maxy = *max_element(arr.begin(), arr.end());
        
        for(auto a:arr){
            if(a<maxy){
                ans = max(a,ans);
            }
        }
        return ans;
        
    }
};

